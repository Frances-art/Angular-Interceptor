import {AfterViewInit, Component, ElementRef, OnDestroy, OnInit, ViewChildren} from '@angular/core';
import {FormBuilder, FormControlName, FormGroup, Validators} from '@angular/forms';
import {fromEvent, merge, Observable, Subscription} from 'rxjs';
import {Bank} from "../bank-bank";
import {BankService} from '../bank.service';
import {ActivatedRoute, Router} from '@angular/router';
import {debounceTime} from 'rxjs/operators';
import {GenericValidator} from "../generic-validator";

@Component({
  selector: 'app-bank-edit',
  templateUrl: './bank-edit.component.html',
  styleUrls: ['./bank-edit.component.scss']
})
export class BankEditComponent implements OnInit, AfterViewInit, OnDestroy {
  @ViewChildren(FormControlName, { read: ElementRef }) formInputElements: ElementRef[];
  pageTitle = 'Bank Edit';
  errorMessage: string;
  //form!: FormGroup;
  bank: Bank = <Bank>{};
  id!: string;
  form!: FormGroup;

  private sub: Subscription;
  displayMessage: { [key: string]: string } = {};
  private validationMessages: { [key: string]: { [key: string]: string } };
  private genericValidator: GenericValidator;


  constructor(private fb: FormBuilder,
              //private form: FormGroup,
              private route: ActivatedRoute,
              private router: Router,
              private bankService: BankService) {
    this.validationMessages = {
      name: {
        required: 'Bank name is required.',
        minlength: 'Bank name must be at least three characters.',
        maxlength: 'Bank name cannot exceed 50 characters.'
      },
      address: {
        required: 'Bank address is required.',
      }
    };

    this.genericValidator = new GenericValidator(this.validationMessages);
  }

  ngOnInit(): void {
    // this.id = this.route.snapshot.params['id'];
    // console.log("Here is ID"+this.id);
    // this.getBank(this.id);
    // this.form = new FormGroup({
    //   name: new FormControl('', [Validators.required]),
    //   acronym: new FormControl('', [Validators.required]),
    //   location: new FormControl('', [Validators.required]),
    //   address: new FormControl('', [Validators.required]),
    //   contact: new FormControl('', [Validators.required]),
    //   email: new FormControl('', [Validators.required]),
    //   website: new FormControl('', [Validators.required]),
    // });



    this.form = this.fb.group({
      name: ['', [Validators.required
      ]],
      address: ['', [Validators.required]],
      contact: '',
      email: '',
      location: ''
    });
    this.sub = this.route.paramMap.subscribe(
      params => {
        const id = params.get('id');
        if (id == '0') {
          const bank: Bank = { id: "0", name: "", address: "", contact: "", email: "", location: "" };
          this.displayBank(bank);
        }
        else {
          this.getBank(id);
        }
      }
    );
  }

  // get f () {
  //   return this.form.controls;
  // }

  ngOnDestroy(): void {
    this.sub.unsubscribe();
  }

  ngAfterViewInit(): void {
    const controlBlurs: Observable<any>[] = this.formInputElements
      .map((formControl: ElementRef) => fromEvent(formControl.nativeElement, 'blur'));

    merge(this.form.valueChanges, ...controlBlurs).pipe(
      debounceTime(800)
    ).subscribe(value => {
      this.displayMessage = this.genericValidator.processMessages(this.form);
    });
  }


  getBank(id: string): void {
    let getBank = this.bankService.getBank(id);
    getBank.subscribe(
      bank => {
        console.log({bank});
        this.displayBank(bank);
      },
      (error: any) => {
        this.errorMessage = <any>error;
        console.error(error);
      });
  }

  displayBank(bank: Bank): void {
    if (this.form) {
      this.form.reset();
    }
    this.bank = bank;

    if (this.bank.id == '0') {
      this.pageTitle = 'Add Bank';
    } else {
      this.pageTitle = `Edit Bank: ${this.bank.name}`;
    }

    this.form.patchValue({
      name: this.bank.name,
      address: this.bank.address,
      contact: this.bank.contact,
      email: this.bank.email,
      location: this.bank.location
    });
  }

  deleteBank(): void {
    if (this.bank.id == '0') {
      this.onSaveComplete();
    } else {
      if (confirm(`Are you sure want to delete this Employee: ${this.bank.name}?`)) {
        this.bankService.deleteBank(this.bank.id)
          .subscribe(
            () => this.onSaveComplete(),
            (error: any) => this.errorMessage = <any>error
          );

      }
    }
  }

  saveBank(): void {
    console.log('trying saving method');
    if (this.form.valid) {
      if (this.form.dirty) {
        const p = { ...this.bank, ...this.form.value };
        if (p.id === '0') {
          p.id = null;
          this.bankService.createBank(p)
            .subscribe((bank)=>this.onSaveComplete(bank));
        } else {
          this.bankService.updateBank(p.id,p)
            .subscribe((bank)=>this.onSaveComplete(bank));
        }
      } else {
        this.onSaveComplete();
      }
    } else {
      this.errorMessage = 'Please correct the validation errors.';
    }
  }

  onSaveComplete(bank?:any): void {
    if(bank.id) {
      this.form.reset();
      this.router.navigate([`/banks/${bank?.id}`]);
    }else{
      console.error("Server return data without ID. Likely false positive response");
      console.error(bank);
    }
  }
}























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































